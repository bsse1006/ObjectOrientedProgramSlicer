package sourcePackage;

public class ExtraExtendedMaths extends ExtendedMaths{

    public ExtraExtendedMaths()
    {

    }

    public int sub(int a, int b) {
        return a-b;
    }

    public int mul(int a, int b) {
        return a*b;
    }

    public int add (int a, int b) {
        return a+b;
    }
}
